import gradio as gr
from transformers import pipeline, set_seed
import nltk
from nltk.sentiment.vader import SentimentIntensityAnalyzer
nltk.download('vader_lexicon')

# ConfiguraciÃ³n inicial
generator = pipeline('text-generation', model='PlanTL-GOB-ES/gpt2-large-bne')  # Usando un modelo GPT-2 en espaÃ±ol
set_seed(42)
sia = SentimentIntensityAnalyzer()

# FunciÃ³n que se conecta con la interfaz
def generar_descripciones(descripcion_base, longitud, opciones):
    resultados = generator(descripcion_base, max_length=longitud, num_return_sequences=opciones)
    respuestas = []

    for res in resultados:
        texto_crudo = res['generated_text']
        texto_limpio = texto_crudo.split(".")[0] + "."
        sentimiento = sia.polarity_scores(texto_limpio)

        if sentimiento['compound'] >= 0.05:
            estado = "ğŸ’¬ Sentimiento: Positivo âœ…"
        elif sentimiento['compound'] <= -0.05:
            estado = "ğŸ’¬ Sentimiento: Negativo âŒ"
        else:
            estado = "ğŸ’¬ Sentimiento: Neutral âšª"

        respuestas.append(f"ğŸ“„ {texto_limpio}\n{estado}")

    return "\n\n".join(respuestas)

# Crear la interfaz visual
interface = gr.Interface(
    fn=generar_descripciones,
    inputs=[
        gr.Textbox(label="âœï¸ Empieza tu descripciÃ³n de producto", placeholder="Este difusor de aromas..."),
        gr.Slider(50, 200, step=10, label="ğŸ“ Longitud del texto"),
        gr.Slider(1, 5, step=1, label="ğŸ” Cantidad de versiones"),
    ],
    outputs=gr.Textbox(label="ğŸ§  Descripciones generadas con IA"),
    title="Generador Inteligente de Descripciones de Producto",
    description="Introduce una idea y deja que la IA escriba por ti. Ideal para tiendas online, e-commerce, dropshipping y Amazon FBA."
)

# Lanzar la app
interface.launch()